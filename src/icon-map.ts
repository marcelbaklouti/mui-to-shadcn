const VARIANT_SUFFIX = /(Outlined|Rounded|Sharp|TwoTone)$/;

// Resolve a MUI icon name (possibly with a style suffix) to a lucide-react name.
export function lucideForMuiIcon(name: string): string | undefined {
  const direct = MUI_ICON_TO_LUCIDE[name];
  if (direct) return direct;
  const base = name.replace(VARIANT_SUFFIX, "");
  if (base !== name) return MUI_ICON_TO_LUCIDE[base];
  return undefined;
}
